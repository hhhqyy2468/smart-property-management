      </template>
    </el-dialog>

    <!-- 缴费对话框 -->
    <el-dialog
      v-model="payDialogVisible"
      title="账单缴费"
      width="500px"
    >
      <el-descriptions :column="1" border>
        <el-descriptions-item label="账单编号">
          {{ payForm.billNo }}
        </el-descriptions-item>
        <el-descriptions-item label="费用类型">
          {{ payForm.feeTypeName }}
        </el-descriptions-item>
        <el-descriptions-item label="应缴金额">
          <span class="amount-text">¥{{ payForm.amountDue?.toFixed(2) }}</span>
        </el-descriptions-item>
        <el-descriptions-item label="缴费方式">
          <el-select v-model="payForm.paymentMethod" style="width: 200px">
            <el-option label="现金" value="cash" />
            <el-option label="银行转账" value="bank" />
            <el-option label="微信支付" value="wechat" />
            <el-option label="支付宝" value="alipay" />
            <el-option label="钱包支付" value="wallet" />
          </el-select>
        </el-descriptions-item>
      </el-descriptions>

      <template #footer>
        <span class="dialog-footer">
          <el-button @click="payDialogVisible = false">取消</el-button>
          <el-button
            type="success"
            :loading="payLoading"
            @click="handlePaySubmit"
          >
            确认缴费
          </el-button>
        </span>
      </template>
    </el-dialog>
  </div>
</template>

<script setup>
import { ref, reactive, onMounted, computed } from 'vue'
import { ElMessage, ElMessageBox } from 'element-plus'
import { Search, Refresh, Plus, Delete, Download, Promotion } from '@element-plus/icons-vue'
import Table from '@/components/Table/index.vue'
import Form from '@/components/Form/index.vue'

// 响应式数据
const searchFormRef = ref()
const tableRef = ref()
const formRef = ref()
const loading = ref(false)
const submitLoading = ref(false)
const generateLoading = ref(false)
const payLoading = ref(false)
const dialogVisible = ref(false)
const generateDialogVisible = ref(false)
const payDialogVisible = ref(false)
const selectedRows = ref([])
const isEdit = ref(false)

// 搜索表单
const searchForm = reactive({
  billNo: '',
  ownerName: '',
  houseCode: '',
  feeTypeId: '',
  billStatus: '',
  billingPeriod: ''
})

// 表格数据
const tableData = ref([])

// 分页配置
const pagination = reactive({
  current: 1,
  pageSize: 10,
  total: 0
})

// 表格列配置
const tableColumns = [
  {
    type: 'selection',
    width: '55'
  },
  {
    prop: 'billNo',
    label: '账单编号',
    width: '160',
    sortable: true
  },
  {
    prop: 'ownerName',
    label: '业主姓名',
    width: '120'
  },
  {
    prop: 'houseCode',
    label: '房间编号',
    width: '140'
  },
  {
    prop: 'feeTypeName',
    label: '费用类型',
    width: '120'
  },
  {
    prop: 'billingPeriod',
    label: '账期',
    width: '100'
  },
  {
    prop: 'amountDue',
    label: '应缴金额',
    width: '120',
    slot: 'amountDue',
    sortable: true
  },
  {
    prop: 'amountPaid',
    label: '实缴金额',
    width: '120',
    slot: 'amountPaid'
  },
  {
    prop: 'billStatus',
    label: '账单状态',
    width: '100',
    slot: 'billStatus'
  },
  {
    prop: 'dueDate',
    label: '缴费截止时间',
    width: '120',
    slot: 'dueDate',
    sortable: true
  },
  {
    prop: 'createTime',
    label: '创建时间',
    width: '180',
    sortable: true
  },
  {
    prop: 'operation',
    label: '操作',
    width: '280',
    slot: 'operation',
    fixed: 'right'
  }
]

// 选项数据
const feeTypeOptions = ref([
  { label: '物业费', value: 1 },
  { label: '停车费', value: 2 },
  { label: '垃圾处理费', value: 3 },
  { label: '电梯费', value: 4 },
  { label: '公共照明费', value: 5 },
  { label: '水费', value: 6 },
  { label: '电费', value: 7 },
  { label: '维修基金', value: 8 }
])

const billStatusOptions = ref([
  { label: '待缴费', value: 0 },
  { label: '已缴费', value: 1 },
  { label: '部分缴费', value: 2 },
  { label: '逾期', value: 3 },
  { label: '已作废', value: 4 }
])

const paymentMethodOptions = ref([
  { label: '现金', value: 'cash' },
  { label: '银行转账', value: 'bank' },
  { label: '微信支付', value: 'wechat' },
  { label: '支付宝', value: 'alipay' },
  { label: '钱包支付', value: 'wallet' }
])

const buildingOptions = ref([
  { label: '1号楼', value: 1 },
  { label: '2号楼', value: 2 },
  { label: '3号楼', value: 3 },
  { label: '4号楼', value: 4 }
])

const unitOptions = ref([
  { label: '1单元', value: 1 },
  { label: '2单元', value: 2 },
  { label: '3单元', value: 3 },
  { label: '4单元', value: 4 }
])

// 表单数据
const form = reactive({
  billId: null,
  billNo: '',
  ownerId: '',
  houseId: '',
  feeTypeId: '',
  billingPeriod: '',
  amountDue: 0,
  dueDate: '',
  remark: ''
})

// 批量生成表单
const generateForm = reactive({
  billingPeriod: '',
  feeTypeId: '',
  generateRange: 1,
  targetIds: []
})

// 缴费表单
const payForm = reactive({
  billId: null,
  billNo: '',
  feeTypeName: '',
  amountDue: 0,
  paymentMethod: 'cash'
})

// 表单规则
const formRules = {
  ownerId: [
    { required: true, message: '请选择业主', trigger: 'change' }
  ],
  houseId: [
    { required: true, message: '请选择房产', trigger: 'change' }
  ],
  feeTypeId: [
    { required: true, message: '请选择费用类型', trigger: 'change' }
  ],
  billingPeriod: [
    { required: true, message: '请选择账期', trigger: 'change' }
  ],
  amountDue: [
    { required: true, message: '请输入应缴金额', trigger: 'blur' },
    { type: 'number', min: 0, message: '金额必须大于等于0', trigger: 'blur' }
  ],
  dueDate: [
    { required: true, message: '请选择缴费截止时间', trigger: 'change' }
  ]
}

// 表单项配置
const formItems = computed(() => [
  {
    prop: 'ownerId',
    label: '业主',
    type: 'input',
    placeholder: '请输入业主姓名'
  },
  {
    prop: 'houseId',
    label: '房产',
    type: 'input',
    placeholder: '请选择房产'
  },
  {
    prop: 'feeTypeId',
    label: '费用类型',
    type: 'select',
    options: feeTypeOptions.value,
    placeholder: '请选择费用类型'
  },
  {
    prop: 'billingPeriod',
    label: '账期',
    type: 'month',
    placeholder: '请选择账期'
  },
  {
    prop: 'amountDue',
    label: '应缴金额',
    type: 'input',
    inputType: 'number',
    placeholder: '请输入应缴金额',
    prepend: '¥'
  },
  {
    prop: 'dueDate',
    label: '缴费截止时间',
    type: 'date',
    placeholder: '请选择缴费截止时间'
  },
  {
    prop: 'remark',
    label: '备注',
    type: 'textarea',
    placeholder: '请输入备注信息'
  }
])

// 计算属性
const dialogTitle = computed(() => isEdit.value ? '编辑账单' : '新增账单')
const targetOptions = computed(() => {
  return generateForm.generateRange === 2 ? buildingOptions.value : unitOptions.value
})

// 获取账单状态名称
const getBillStatusName = (status) => {
  const option = billStatusOptions.value.find(item => item.value === status)
  return option ? option.label : '未知'
}

// 获取账单状态标签
const getBillStatusTag = (status) => {
  const tagMap = {
    0: 'warning', // 待缴费
    1: 'success', // 已缴费
    2: 'info',    // 部分缴费
    3: 'danger',  // 逾期
    4: 'info'     // 已作废
  }
  return tagMap[status] || 'info'
}

// 获取实缴金额样式
const getPaidAmountClass = (paid, due) => {
  if (paid >= due) return 'paid-full'
  if (paid > 0) return 'paid-partial'
  return 'paid-none'
}

// 判断是否逾期
const isOverdue = (dueDate, status) => {
  if (status === 1) return false // 已缴费不算逾期
  return new Date(dueDate) < new Date()
}

// 获取模拟数据
const getMockData = () => {
  const mockBills = []
  const owners = ['张三', '李四', '王五', '赵六', '钱七', '孙八', '周九', '吴十']
  const statuses = [0, 1, 2, 3]
  const currentYear = new Date().getFullYear()
  const currentMonth = new Date().getMonth() + 1

  for (let i = 0; i < 100; i++) {
    const feeType = feeTypeOptions.value[Math.floor(Math.random() * feeTypeOptions.value.length)]
    const status = statuses[Math.floor(Math.random() * statuses.length)]
    const amountDue = 50 + Math.floor(Math.random() * 500)
    const amountPaid = status === 1 ? amountDue : (status === 2 ? Math.floor(amountDue * 0.6) : 0)
    const monthOffset = Math.floor(Math.random() * 6) // 最近6个月
    const billingMonth = currentMonth - monthOffset
    const billingYear = billingMonth > 0 ? currentYear : currentYear - 1
    const actualMonth = billingMonth > 0 ? billingMonth : billingMonth + 12

    mockBills.push({
      billId: i + 1,
      billNo: `BILL${(i + 1).toString().padStart(6, '0')}`,
      ownerName: owners[i % owners.length],
      houseCode: `H${Math.floor(Math.random() * 4 + 1)}${Math.floor(Math.random() * 18 + 1)}${Math.floor(Math.random() * 3 + 1)}`,
      feeTypeName: feeType.label,
      feeTypeId: feeType.value,
      billingPeriod: `${billingYear}-${actualMonth.toString().padStart(2, '0')}`,
      amountDue: amountDue,
      amountPaid: amountPaid,
      billStatus: status,
      dueDate: `${billingYear}-${actualMonth.toString().padStart(2, '0')}-25`,
      createTime: '2024-01-01 10:00:00'
    })
  }

  // 模拟分页
  pagination.total = mockBills.length
  return mockBills
}

// 搜索
const handleSearch = () => {
  pagination.current = 1
  fetchData()
}

// 重置
const handleReset = () => {
  searchFormRef.value?.resetFields()
  handleSearch()
}

// 获取数据
const fetchData = async () => {
  loading.value = true
  try {
    // 模拟API请求
    setTimeout(() => {
      tableData.value = getMockData()
      loading.value = false
    }, 500)
  } catch (error) {
    loading.value = false
    ElMessage.error('获取数据失败')
  }
}

// 分页变化
const handlePageChange = (page) => {
  pagination.current = page
  fetchData()
}

// 排序变化
const handleSortChange = (sort) => {
  console.log('排序变化:', sort)
  fetchData()
}

// 选择变化
const handleSelectionChange = (selection) => {
  selectedRows.value = selection
}

// 新增
const handleAdd = () => {
  isEdit.value = false
  resetForm()
  dialogVisible.value = true
}

// 编辑
const handleEdit = (row) => {
  isEdit.value = true
  Object.assign(form, { ...row })
  dialogVisible.value = true
}

// 删除
const handleDelete = async (row) => {
  try {
    await ElMessageBox.confirm(
      `确定要删除账单"${row.billNo}"吗？`,
      '提示',
      { type: 'warning' }
    )
    ElMessage.success('删除成功')
    fetchData()
  } catch (error) {
    // 用户取消操作
  }
}

// 批量删除
const handleBatchDelete = async () => {
  try {
    await ElMessageBox.confirm(
      `确定要删除选中的${selectedRows.value.length}个账单吗？`,
      '提示',
      { type: 'warning' }
    )
    ElMessage.success('批量删除成功')
    fetchData()
  } catch (error) {
    // 用户取消操作
  }
}

// 批量生成账单
const handleBatchGenerate = () => {
  Object.assign(generateForm, {
    billingPeriod: '',
    feeTypeId: '',
    generateRange: 1,
    targetIds: []
  })
  generateDialogVisible.value = true
}

// 提交批量生成
const handleGenerateSubmit = async () => {
  if (!generateForm.billingPeriod || !generateForm.feeTypeId) {
    ElMessage.warning('请填写完整的生成条件')
    return
  }

  if (generateForm.generateRange > 1 && generateForm.targetIds.length === 0) {
    ElMessage.warning('请选择生成目标')
    return
  }

  generateLoading.value = true
  try {
    // 模拟API请求
    setTimeout(() => {
      ElMessage.success('批量生成账单成功')
      generateDialogVisible.value = false
      fetchData()
      generateLoading.value = false
    }, 2000)
  } catch (error) {
    generateLoading.value = false
  }
}

// 缴费
const handlePay = (row) => {
  Object.assign(payForm, {
    billId: row.billId,
    billNo: row.billNo,
    feeTypeName: row.feeTypeName,
    amountDue: row.amountDue,
    paymentMethod: 'cash'
  })
  payDialogVisible.value = true
}

// 提交缴费
const handlePaySubmit = async () => {
  payLoading.value = true
  try {
    // 模拟API请求
    setTimeout(() => {
      ElMessage.success('缴费成功')
      payDialogVisible.value = false
      fetchData()
      payLoading.value = false
    }, 1000)
  } catch (error) {
    payLoading.value = false
  }
}

// 查看详情
const handleViewDetail = (row) => {
  ElMessage.info(`查看账单${row.billNo}的详细信息`)
}

// 导出
const handleExport = () => {
  ElMessage.success('导出成功')
}

// 提交表单
const handleSubmit = async () => {
  if (!formRef.value) return

  try {
    await formRef.value.validate()
    submitLoading.value = true

    // 模拟API请求
    setTimeout(() => {
      ElMessage.success(isEdit.value ? '编辑成功' : '新增成功')
      dialogVisible.value = false
      fetchData()
      submitLoading.value = false
    }, 1000)
